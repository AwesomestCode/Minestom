package com.minestom.code_generation.entity;

import com.google.gson.*;
import com.google.gson.stream.JsonReader;
import com.minestom.code_generation.MinestomCodeGenerator;
import com.squareup.javapoet.*;
import org.jetbrains.annotations.NotNull;
import org.jetbrains.annotations.Nullable;
import org.slf4j.Logger;
import org.slf4j.LoggerFactory;

import javax.lang.model.element.Modifier;
import java.io.File;
import java.io.FileNotFoundException;
import java.io.FileReader;
import java.util.Collections;
import java.util.Objects;

public final class VillagerTypeGenerator extends MinestomCodeGenerator {
    private static final Logger LOGGER = LoggerFactory.getLogger(VillagerTypeGenerator.class);
    private static final Gson GSON = new GsonBuilder().setPrettyPrinting().disableHtmlEscaping().create();
    private static final File DEFAULT_INPUT_FILE = new File(DEFAULT_SOURCE_FOLDER_ROOT + "/json", "villager_types.json");
    private final File villagerTypesFile;
    private final File outputFolder;

    public VillagerTypeGenerator() {
        this(null, null);
    }

    public VillagerTypeGenerator(@Nullable File villagerTypesFile) {
        this(villagerTypesFile, null);
    }

    public VillagerTypeGenerator(@Nullable File villagerTypesFile, @Nullable File outputFolder) {
        this.villagerTypesFile = Objects.requireNonNullElse(villagerTypesFile, DEFAULT_INPUT_FILE);
        this.outputFolder = Objects.requireNonNullElse(outputFolder, DEFAULT_OUTPUT_FOLDER);
    }

    @Override
    public void generate() {
        if (!villagerTypesFile.exists()) {
            LOGGER.error("Failed to find villager_types.json.");
            LOGGER.error("Stopped code generation for villager types.");
            return;
        }
        if (!outputFolder.exists() && !outputFolder.mkdirs()) {
            LOGGER.error("Output folder for code generation does not exist and could not be created.");
            return;
        }
        // Important classes we use alot
        ClassName namespaceIDClassName = ClassName.get("net.minestom.server.utils", "NamespaceID");
        ClassName keyIDClassName = ClassName.get("net.kyori.adventure.key", "Key");

        JsonArray villagerTypes;
        try {
            villagerTypes = GSON.fromJson(new JsonReader(new FileReader(villagerTypesFile)), JsonArray.class);
        } catch (FileNotFoundException e) {
            LOGGER.error("Failed to find villager_types.json.");
            LOGGER.error("Stopped code generation for villager types.");
            return;
        }
        ClassName villagerTypeClassName = ClassName.get("net.minestom.server.entity.metadata.villager", "VillagerType");

        // Particle
        TypeSpec.Builder villagerTypeClass = TypeSpec.classBuilder(villagerTypeClassName)
                .addSuperinterface(ClassName.get("net.kyori.adventure.key", "Keyed"))
                .addModifiers(Modifier.PUBLIC).addJavadoc("AUTOGENERATED");
        villagerTypeClass.addField(
                FieldSpec.builder(namespaceIDClassName, "id")
                        .addModifiers(Modifier.PRIVATE, Modifier.FINAL).addAnnotation(NotNull.class).build()
        );
        villagerTypeClass.addMethod(
                MethodSpec.constructorBuilder()
                        .addParameter(ParameterSpec.builder(namespaceIDClassName, "id").addAnnotation(NotNull.class).build())
                        .addStatement("this.id = id")
                        .addModifiers(Modifier.PROTECTED)
                        .build()
        );
        // Override key method (adventure)
        villagerTypeClass.addMethod(
                MethodSpec.methodBuilder("key")
                        .returns(keyIDClassName)
                        .addAnnotation(Override.class)
                        .addAnnotation(NotNull.class)
                        .addStatement("return this.id")
                        .addModifiers(Modifier.PUBLIC)
                        .build()
        );
        // getId method
        villagerTypeClass.addMethod(
                MethodSpec.methodBuilder("getId")
                        .returns(namespaceIDClassName)
                        .addAnnotation(NotNull.class)
                        .addStatement("return this.id")
                        .addModifiers(Modifier.PUBLIC)
                        .build()
        );
        CodeBlock.Builder code = CodeBlock.builder();
        // Use data
        for (JsonElement vp : villagerTypes) {
            JsonObject villagerProfession = vp.getAsJsonObject();

            String villagerProfessionName = villagerProfession.get("name").getAsString();

            villagerTypeClass.addField(
                    FieldSpec.builder(
                            villagerTypeClassName,
                            villagerProfessionName
                    ).initializer(
                            "new $T($T.from($S))",
                            villagerTypeClassName,
                            namespaceIDClassName,
                            villagerProfession.get("id").getAsString()
                    ).addModifiers(Modifier.PUBLIC, Modifier.STATIC, Modifier.FINAL).build()
            );
            ClassName registryClassName = ClassName.get("net.minestom.server.registry", "Registries");
            // Add to static init.
            code.addStatement("$T.registerVillagerType($N.getId(), $N)", registryClassName, villagerProfessionName, villagerProfessionName);
        }

        villagerTypeClass.addStaticBlock(code.build());

        // Write files to outputFolder
        writeFiles(
                Collections.singletonList(
                        JavaFile.builder("net.minestom.server.entity.metadata.villager", villagerTypeClass.build()).build()
                ),
                outputFolder
        );
    }
}
