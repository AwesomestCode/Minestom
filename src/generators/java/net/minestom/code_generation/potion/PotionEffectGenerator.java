package net.minestom.code_generation.potion;

import com.google.gson.Gson;
import com.google.gson.GsonBuilder;
import com.google.gson.JsonArray;
import com.google.gson.JsonElement;
import com.google.gson.JsonObject;
import com.google.gson.stream.JsonReader;
import net.minestom.code_generation.MinestomCodeGenerator;
import com.squareup.javapoet.*;
import org.jetbrains.annotations.NotNull;
import org.jetbrains.annotations.Nullable;
import org.slf4j.Logger;
import org.slf4j.LoggerFactory;

import javax.lang.model.element.Modifier;
import java.io.File;
import java.io.FileNotFoundException;
import java.io.FileReader;
import java.util.Collections;
import java.util.List;
import java.util.Objects;

public final class PotionEffectGenerator extends MinestomCodeGenerator {
    private static final Logger LOGGER = LoggerFactory.getLogger(PotionEffectGenerator.class);
    private static final Gson GSON = new GsonBuilder().setPrettyPrinting().disableHtmlEscaping().create();
    private static final File DEFAULT_INPUT_FILE = new File(DEFAULT_SOURCE_FOLDER_ROOT + "/json", "potion_effects.json");
    private final File potionEffectsFile;
    private final File outputFolder;

    public PotionEffectGenerator() {
        this(null, null);
    }

    public PotionEffectGenerator(@Nullable File potionEffectsFile) {
        this(potionEffectsFile, null);
    }

    public PotionEffectGenerator(@Nullable File potionEffectsFile, @Nullable File outputFolder) {
        this.potionEffectsFile = Objects.requireNonNullElse(potionEffectsFile, DEFAULT_INPUT_FILE);
        this.outputFolder = Objects.requireNonNullElse(outputFolder, DEFAULT_OUTPUT_FOLDER);
    }

    @Override
    public void generate() {
        if (!potionEffectsFile.exists()) {
            LOGGER.error("Failed to find potionEffects.json.");
            LOGGER.error("Stopped code generation for potion effects.");
            return;
        }
        if (!outputFolder.exists() && !outputFolder.mkdirs()) {
            LOGGER.error("Output folder for code generation does not exist and could not be created.");
            return;
        }
        // Important classes we use alot
        ClassName namespaceIDClassName = ClassName.get("net.minestom.server.utils", "NamespaceID");

        JsonArray potionEffects;
        try {
            potionEffects = GSON.fromJson(new JsonReader(new FileReader(potionEffectsFile)), JsonArray.class);
        } catch (FileNotFoundException e) {
            LOGGER.error("Failed to find potionEffects.json.");
            LOGGER.error("Stopped code generation for potion effects.");
            return;
        }
        ClassName potionEffectClassName = ClassName.get("net.minestom.server.potion", "PotionEffect");

        // Particle
        TypeSpec.Builder potionEffectClass = TypeSpec.classBuilder(potionEffectClassName)
                .addSuperinterface(ClassName.get("net.kyori.adventure.key", "Keyed"))
                .addModifiers(Modifier.PUBLIC).addJavadoc("AUTOGENERATED");
        potionEffectClass.addField(
                FieldSpec.builder(namespaceIDClassName, "id")
                        .addModifiers(Modifier.PRIVATE, Modifier.FINAL).addAnnotation(NotNull.class).build()
        );
        potionEffectClass.addField(
                FieldSpec.builder(TypeName.INT, "color")
                        .addModifiers(Modifier.PRIVATE, Modifier.FINAL).build()
        );
        potionEffectClass.addField(
                FieldSpec.builder(TypeName.BOOLEAN, "instantaneous")
                        .addModifiers(Modifier.PRIVATE, Modifier.FINAL).build()
        );
        potionEffectClass.addMethod(
                MethodSpec.constructorBuilder()
                        .addParameter(ParameterSpec.builder(namespaceIDClassName, "id").addAnnotation(NotNull.class).build())
                        .addParameter(ParameterSpec.builder(TypeName.INT, "color").build())
                        .addParameter(ParameterSpec.builder(TypeName.BOOLEAN, "instantaneous").build())
                        .addStatement("this.id = id")
                        .addStatement("this.color = color")
                        .addStatement("this.instantaneous = instantaneous")
                        .addModifiers(Modifier.PROTECTED)
                        .build()
        );
        // Override key method (adventure)
        potionEffectClass.addMethod(
                MethodSpec.methodBuilder("key")
                        .returns(ClassName.get("net.kyori.adventure.key", "Key"))
                        .addAnnotation(Override.class)
                        .addAnnotation(NotNull.class)
                        .addStatement("return this.id")
                        .addModifiers(Modifier.PUBLIC)
                        .build()
        );
        // getId method
        potionEffectClass.addMethod(
                MethodSpec.methodBuilder("getId")
                        .returns(namespaceIDClassName)
                        .addAnnotation(NotNull.class)
                        .addStatement("return this.id")
                        .addModifiers(Modifier.PUBLIC)
                        .build()
        );
        // getColor method
        potionEffectClass.addMethod(
                MethodSpec.methodBuilder("getColor")
                        .returns(TypeName.INT)
                        .addStatement("return this.color")
                        .addModifiers(Modifier.PUBLIC)
                        .build()
        );
        // isInstantaneous method
        potionEffectClass.addMethod(
                MethodSpec.methodBuilder("isInstantaneous")
                        .returns(TypeName.BOOLEAN)
                        .addStatement("return this.instantaneous")
                        .addModifiers(Modifier.PUBLIC)
                        .build()
        );
        // getNumericalId
        potionEffectClass.addMethod(
                MethodSpec.methodBuilder("getNumericalId")
                        .returns(TypeName.INT)
                        .addStatement(
                                "return $T.getPotionEffectId(this)",
                                ClassName.get("net.minestom.server.registry", "Registries")
                        )
                        .addModifiers(Modifier.PUBLIC)
                        .build()
        );
        // fromId Method
        potionEffectClass.addMethod(
                MethodSpec.methodBuilder("fromId")
                        .returns(potionEffectClassName)
                        .addAnnotation(Nullable.class)
                        .addParameter(TypeName.INT, "id")
                        .addStatement(
                                "return $T.getPotionEffect(id)",
                                ClassName.get("net.minestom.server.registry", "Registries")
                        )
                        .addModifiers(Modifier.PUBLIC, Modifier.STATIC)
                        .build()
        );
        // values method
        potionEffectClass.addMethod(
                MethodSpec.methodBuilder("values")
                        .returns(ParameterizedTypeName.get(ClassName.get(List.class), potionEffectClassName))
                        .addStatement("return $T.getPotionEffects()", ClassName.get("net.minestom.server.registry", "Registries"))
                        .addModifiers(Modifier.PUBLIC, Modifier.STATIC)
                        .build()
        );

        CodeBlock.Builder staticBlock = CodeBlock.builder();
        // Use data
        for (JsonElement pe : potionEffects) {
            JsonObject potionEffect = pe.getAsJsonObject();

            String potionEffectName = potionEffect.get("name").getAsString();

            potionEffectClass.addField(
                    FieldSpec.builder(
                            potionEffectClassName,
                            potionEffectName
                    ).initializer(
                            "new $T($T.from($S), $L, $L)",
                            potionEffectClassName,
                            namespaceIDClassName,
                            potionEffect.get("id").getAsString(),
                            potionEffect.get("color").getAsInt(),
                            potionEffect.get("instantaneous").getAsBoolean()
                    ).addModifiers(Modifier.PUBLIC, Modifier.STATIC, Modifier.FINAL).build()
            );
            // Add to static init.
            staticBlock.addStatement("$T.registerPotionEffect($N)", ClassName.get("net.minestom.server.registry", "Registries"), potionEffectName);
        }

        potionEffectClass.addStaticBlock(staticBlock.build());

        // Write files to outputFolder
        writeFiles(
                Collections.singletonList(
                        JavaFile.builder("net.minestom.server.potion", potionEffectClass.build()).build()
                ),
                outputFolder
        );
    }
}
